    public void tampil(){
        try {
            String [] head_tbl ={"ID","USERNAME","PASSWORD"};
            model = new DefaultTableModel(head_tbl,0);
            tbl_admin.setModel(model);
            
            int row = tbl_admin.getRowCount();
            for (int i=1;i<row;i++){
                model.removeRow(0);
            }
            String sqltampil = "SELECT * FROM user";
            ResultSet rs = conn.createStatement().executeQuery(sqltampil);
            while(rs.next()){
                String [] data = {rs.getString(1),rs.getString(2),rs.getString(3)};
                model.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(padmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void reset(){
        tf_admin_user.setText("");
        tf_admin_pass.setText("");
        tampil();
    }
