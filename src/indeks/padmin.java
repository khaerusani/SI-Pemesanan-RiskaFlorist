    private void btn_admin_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_admin_ubahActionPerformed
        try {
            String sqlubah = "UPDATE user SET username=?,pass=? WHERE id_user=?";
            PreparedStatement ps = conn.prepareStatement(sqlubah);
            ps.setString(1, tf_admin_user.getText());
            ps.setString(2, tf_admin_pass.getText());
            ps.setString(3, lbl_id.getText());
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Edit Berhasil");
            reset();
        } catch (SQLException ex) {
            Logger.getLogger(padmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_admin_ubahActionPerformed

