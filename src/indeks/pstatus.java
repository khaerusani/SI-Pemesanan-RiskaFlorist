    private void btn_stat_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_stat_cariActionPerformed
        try {
            String [] head_tbl = {"ID Pemesanan","Nama Pemesan","Biaya","Tgl Pesan","Tgl Kirim","Tgl Bayar","Nama Tujuan","Pembayaran"};
            model = new DefaultTableModel(head_tbl, 0);
            tbl_stat.setModel(model);
            int row = tbl_stat.getRowCount();
            for (int i=1;i<row;i++){
                model.removeRow(0);
            }
            String sql_stat_tampil = "SELECT pesan.id_pesan, pesan.nama_pesan, bunga.harga, pesan.tgl_pesan, pesan.tgl_kirim, bayar.tgl_bayar, pesan.stat_pesan, bayar.status_pembayaran"
                    + "               FROM pesan,bunga,bayar "
                    + "               WHERE pesan.id_pesan=bayar.id_pesan AND pesan.kd_bunga=bunga.kd_bunga "
                    + "               AND (pesan.id_pesan=? OR pesan.nama_pesan=? OR pesan.tgl_pesan=? OR pesan.tgl_kirim=? "
                    + "               OR bayar.tgl_bayar=? OR pesan.stat_pesan=? OR bayar.status_pembayaran=?) "
                    + "               GROUP BY pesan.id_pesan ORDER BY pesan.id_pesan";
            
            PreparedStatement ps = conn.prepareStatement(sql_stat_tampil);
            ps.setString(1, tf_stat_cari.getText());
            ps.setString(2, tf_stat_cari.getText());
            ps.setString(3, tf_stat_cari.getText());
            ps.setString(4, tf_stat_cari.getText());
            ps.setString(5, tf_stat_cari.getText());
            ps.setString(6, tf_stat_cari.getText());
            ps.setString(7, tf_stat_cari.getText());
            ResultSet rss = ps.executeQuery();
            while(rss.next()){
                String data[] = {rss.getString(1),rss.getString(2),rss.getString(3),rss.getString(4),rss.getString(5),rss.getString(6),rss.getString(7),rss.getString(8)};
                model.addRow(data);
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(pstatus.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btn_stat_cariActionPerformed

