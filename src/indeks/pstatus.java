    public void lapor(){
        if(batas1 == null || batas2 == null){
            laporan lap = new laporan();
            lap.setVisible(true);
            Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
            lap.setLocation(dim.width/2-lap.getSize().width/2, dim.height/2-lap.getSize().height/2);
        } 
        else{
        try {
            InputStream f = getClass().getResourceAsStream("/report/laporanbulan.jrxml");
            //File f=new File("src/report/laporanbulan.jrxml");
            jasperDesign = JRXmlLoader.load(f);
            //param.clear();
            //param.put("nama", tf_crud_cetak.getText());
            param.put("parameter1", batas1);
            param.put("parameter2", batas2);
            jasperReport = JasperCompileManager.compileReport(jasperDesign);
            jasperPrint = JasperFillManager.fillReport(jasperReport, param, conn);
            jasperPrint.setName("Laporan Bulanan");
            JasperViewer.viewReport(jasperPrint, false);
        } catch (JRException ex) {
            Logger.getLogger(pstatus.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    
    }    
