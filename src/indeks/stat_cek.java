        public Boolean cekinput(){
        if(tf_bayar.getText() == null
           || tf_harga.getText() == null
           || tf_kd.getText() == null
           || tf_kirim.getText() == null
           || tf_konten.getText() == null
           || tf_nama.getText() == null
           || tf_pemesan.getText() == null
           || tf_penagihan.getText() == null
           || tf_pesan.getText() == null
           || tf_telp.getText() == null){
            return false;
        
        } else{
            return true;
        }
    }
    
