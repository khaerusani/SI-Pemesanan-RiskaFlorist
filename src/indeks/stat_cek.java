    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        if(cekinput()){
            try {
                String sqlupdate = "UPDATE pesan,bayar SET pesan.nama_pesan=?,pesan.telp_pesan=?,pesan.alamat_pesan=?,"
                        + "pesan.alamat_tujuan=?,pesan.konten=?,pesan.stat_pesan=?,pesan.kd_bunga=?,bayar.status_pembayaran=?,bayar.alamat_tagih=? "
                        + "WHERE pesan.id_pesan=bayar.id_pesan AND pesan.id_pesan=?";
                PreparedStatement ps = conn.prepareStatement(sqlupdate);
                ps.setString(1, tf_nama.getText());
                ps.setString(2, tf_telp.getText());
                ps.setString(3, tf_pemesan.getText());
                ps.setString(4, tf_kirim.getText());
                ps.setString(5, tf_konten.getText());
                ps.setString(6, tf_pesan.getText());
                ps.setString(7, tf_kd.getText());
                ps.setString(8, tf_bayar.getText());
                ps.setString(9, tf_penagihan.getText());
                ps.setString(10, tf_id.getText());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data tersimpan\nKlik Refresh Untuk Update");
                this.dispose();
                
            } catch (SQLException ex) {
                Logger.getLogger(stat_cek.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex){
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Kesalahan Tak Diketahui", JOptionPane.ERROR_MESSAGE);
            }
        
        } else {
            JOptionPane.showMessageDialog(null, "Ada Form Kosong");
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

